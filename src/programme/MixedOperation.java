package programme;

public class MixedOperation {
    public static void main(String[] args) {
        //Mixed operation declaration
        int a = -5 + 8 * 6;
        int b = (55 + 9) % 9;
        int c = 20 + -3 * 5 / 8;
        int d = 5 + 15 / 3 * 2 - 8 % 3;
        //Output
        System.out.println(-5 + 8 * 6 );
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }

}
