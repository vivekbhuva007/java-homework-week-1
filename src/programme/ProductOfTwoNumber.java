package programme;

public class ProductOfTwoNumber {
    public static void main(String[] args) {
        // SEVEN Number declaration
        int sevenNumber = 25;


        //Second Number declaration
        int eightNumber = 5;

        int produced = sevenNumber * eightNumber;
        //Expected output
        System.out.println(sevenNumber + " x " + eightNumber + " = " + produced);
    }
}

